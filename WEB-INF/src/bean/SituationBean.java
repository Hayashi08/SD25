package bean;

public class SituationBean {
    
    private String id = "";
    private String userId = "";
    private String floorId = "";
    private int qty = 0;
    private String date = "";
    private String start = "";
    private String endSchedule = "";
    private String end = "";
    private String freeString = "";
    
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getUserId() {
        return userId;
    }
    public void setUserId(String userId) {
        this.userId = userId;
    }
    public String getFloorId() {
        return floorId;
    }
    public void setFloorId(String floorId) {
        this.floorId = floorId;
    }
    public int getQty() {
        return qty;
    }
    public void setQty(int qty) {
        this.qty = qty;
    }
    public String getDate() {
        return date;
    }
    public void setDate(String date) {
        this.date = date;
    }
    public String getStart() {
        return start;
    }
    public void setStart(String start) {
        this.start = start;
    }
    public String getEndSchedule() {
        return endSchedule;
    }
    public void setEndSchedule(String endSchedule) {
        this.endSchedule = endSchedule;
    }
    public String getEnd() {
        return end;
    }
    public void setEnd(String end) {
        this.end = end;
    }
    public String getFreeString() {
        return freeString;
    }
    public void setFreeString(String freeString) {
        this.freeString = freeString;
    }

}
